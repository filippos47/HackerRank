void merge(vector<int> &arr, int l, int m, int r) {
    int i, j, k;
    int n1 = m - l + 1, n2 = r - m;;
    vector<int> L(n1), R(n2);

    for(i = 0; i < n1; i++) {
        L[i] = arr[l + i];
    }

    for(j = 0; j < n2; j++) {
        R[j] = arr[m + 1 + j];
    }

    i = 0;
    j = 0;
    k = l;

    while(i < n1 && j < n2) {
        if(L[i] < R[j]) {
            arr[k] = L[i];
            i++;
        }
        else {
            arr[k] = R[j];
            j++;
        }
        k++;
    }

    while(i < n1) {
        arr[k] = L[i];
        i++;
        k++;
    }

    while(j < n2) {
        arr[k] = R[j];
        j++;
        k++;
    }
}

void mergeSort(vector<int> &arr, int l, int r) {
    if(l < r) {
        int m = l + (r - l) / 2;

        mergeSort(arr, l, m);
        mergeSort(arr, m + 1, r);

        merge(arr, l, m, r);
    }
}

// Complete the getMinimumCost function below.
int getMinimumCost(int k, vector<int> c) {
    int minCost = 0, cycle = 1, timesGone = 1;

    mergeSort(c, 0, c.size() - 1);
    for(int i = c.size() - 1; i > -1; i--) {
        minCost += timesGone * c[i];
        cycle++;
        if(cycle > k) {
            timesGone++;
            cycle = 1;
        }
    }

    return minCost;
}

